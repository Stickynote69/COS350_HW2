package Homework2;

public class Pogo {
	private int jump;
	private int cost;
	
	public Pogo() {
		this.jump = -1;
		this.cost = -1;
	}
	
	public int getJump() {return this.jump;}
	public void setJump(int jump) {this.jump = jump;}
	public int getCost() {return this.cost;}
	public void setCost(int cost) {this.cost = cost;}
}
